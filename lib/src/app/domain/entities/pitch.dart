class Pitch {
  const Pitch({
    required this.id,
    required this.venueId,
    required this.venueName,
    required this.code,
    required this.name,
    required this.description,
    required this.type,
    required this.size,
    required this.surface,
    required this.status,
  });

  final String id;
  final String venueId;
  final String venueName;
  final String code;
  final String name;
  final String description;
  final String type;
  final String size;
  final String surface;
  final String status;
}
